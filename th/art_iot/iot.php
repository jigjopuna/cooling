					<h2>ระบบห้องเย็นออนไลน์</h2>
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
					ระบบห้องเย็นอัจฉริยะหรือ <span class="text-strong">IoT</span> จะช่วยเจ้าของห้องเย็นดูและห้องเย็น ห้องเย็นไม่เย็น เสีย หรือมีปัญหาเราจะเฝ้าดูให้ตลอดเวลา 24 ชั่วโมง ผ่านระบบออนไลน์ ไม่ว่าจะมีปัญหาจากอะไรก็ตาม ทาง Topcooling
					จะวิเคราะห์และหาสาเหตุต้นตอของปัญหา เพื่อแก้ไข แล้วเจ้า IoT ที่ว่ามานี้จะเข้ามาช่วยเรายังไง ติดตามในบทความนี้ครับ<br><br>
					
					เชื่อว่าใครหลายๆ คน ที่มีห้องเย็นแล้วหรือกำลังจะตัดสินใจซื้อ จากประสบการณ์ 30 ปี ของผมจะมา <span class="text-strong">แชร์ปัญหา</span> และวิธีการแก้ไขปัญหาที่ทุกคนต้องเคยเจอ ต่อจากนี้<br><br>
					
					
					<span class="topic-article"> 1. ห้องเย็นเสีย ห้องเย็นไม่เย็น</span><br>
					เป็นปัญหาพบเจอบ่อยครั้งมาก เวลาที่ลูกค้าโทรหาช่าง ฮัลโลครับช่าง <span class="text-strong">ห้องเย็นเสีย</span> ไม่เย็นรีบมาหน่อยได้ไหมของจะเสีย ช่างก็ถามอาการที่มีปัญหา เช่น น้ำยาหมดหรือเปล่าถ้าหมดไวกว่าปกติให้ไปดูว่าท่อน้ำยา
					รั่วไหมอาจมีตะกร้าสินค้าไปกระแทกท่อน้ำยาแตก<br><br>
					เครื่องทำงานปกติหรือมีเสียงแปลกๆ ไหม หากทำงานปกติและน้ำยาไม่รั่ว ก็ดูต่อว่าคอล์ยเย็นมีน้ำแข็งตันไหม เป็นต้น จริงๆ มีอีกหลายวิธีการตรวจสอบ<br><br>
					
					<span class="text-strong">หากมีระบบ IoT</span> เข้ามาช่วย ปัญหาเหล่านี้จะช่วยบรรเทาไปได้ เพราะเราจะ Monitor คอยเฝ้าดูให้ตลอดเวลาด้วยแอปพลิเคชั่นออนไลน์ จะแจ้งเตือนทันทีที่มีปัญหา ช่างจะได้เตรียมการแก้ไขปัญหาได้ทันท่วงที
					ระบบที่มีก็คือจะแจ้งเตือนผ่าน Line ที่เราใช้กันอยู่แล้วทุกวัน จะแจ้งไปหาลูกค้าและ TCL ช่วยกันดูหากอุณหภูมิห้องเย็นผิดปกติ  นอกจากนี้ยังมีข้อมูลอุณหภูมิย้อนหลังได้ผ่านกราฟบนมือถือซึ่งดูได้ตลอดเวลา
					
					<br><br>
					<img src="../../content/images/art_iot/iot/line.jpg" alt="Line ห้องเย็น"><br><br>
					
					ในรูป Line และรูปนี้ เราจะเก็บค่าอุณหภูมิทุกๆ 5 นาที ขึ้นระบบ Cloud คลาวน์ แล้วนำมาแสดงเป็นกราฟ ถ้าอุณหภูมิไม่ได้ตามทีกำหนดไว้ก็จะมีการแจ้งเตือนมาใน Line<br><br>
					
					<img src="../../content/images/art_iot/iot/temp0.jpg" alt="อุณหภูมิห้องเย็น"><br><br>
					
					<span class="topic-article"> 2. ไม่รู้ว่าห้องเย็นจะเสียตอนไหน</span><br>
					ลองคิดเล่นๆ ดูว่า ถ้าเราแช่ไวน์ ราคาแพงๆ หลายๆ ขวดไว้ในห้องเย็น แล้วเกิดห้องเย็นไม่เย็นตอนเวลาตี 2 ขึ้นมา เราจะไม่รู้เลยว่าห้องเย็นไม่เย็นจนกว่าจะเช้าและเข้าไปดู ปรากฎว่าไวน์เสียไปแล้ว<br><br>
					
					<strong><u>ห้องเย็นราคาหลักแสน แต่ไวน์และสินค้าหลักล้าน</u></strong><br><br>
					
					ถ้าเรามีระบบ IoT จะแจ้งเตือนปัญหานี้ตอนตี 2 เลยทันที แจ้งมาทาง Line ของลูกค้าและทางเรา<br><br>
					
					
					<span class="topic-article"> 3. หาช่างซ่อมห้องเย็นไม่ได้</span><br>
					เนื่องจากว่าระบบทำความเย็นห้องเย็นจะต้องใช้ช่าง <span class="text-strong">เฉพาะทาง</span> หากหาช่างห้องเย็นไม่ได้ หลายๆ คนก็เลยคิดหาช่างแอร์มาซ่อมแทนคิดว่าจะน่าคล้ายๆ กัน ผมก็เคยคิดอย่างนั้น แต่ระบบแอร์กับระบบห้องเย็น
					อุปกรณ์แต่ละอย่างจะไม่เหมือนกันซะทีเดียว เพราะแอร์อุณหภูมิไม่ต่ำมาก อย่างต่ำสุดก็ 15 องศา แต่ห้องเย็นอุณหภูมิต่ำสุดอยู่ที่ -18 องศา<br><br>
					
					เมื่อเกิดปัญหาอยู่ที่ว่าเราจะ <span class="text-strong">เรียกช่าง</span> ได้เร็วแค่ไหน เพื่อไม่ให้สินค้าเสีย<br><br>
					
					ถ้าเรามีระบบ IoT จะช่วยลดความเสี่ยง ประหยัดเวลาและค่าใช้จ่าย เพราะเรารู้ปัญหาก่อนที่สินค้าจะเสียหาย และช่างมาได้ทันเวลา<br><br>
					
					
					
					<span class="topic-article"> 4. Data Logger ยังต้องมีคนคอยดูแล</span><br>
					หลายๆ บริษัท ใช้ <span class="text-strong">Data Logger</span> ดาต้าล็อคเกอร์ เพื่อที่จะดูรายงานอุณหภูมิภายในห้องเย็น ข้อจำกัดจะอยู่ตรงที่ต้องนำ Data Logger มาดูข้อมูลในคอมพิวเตอร์ ดูผ่านมือถือไม่ได้ ไม่สามารถดูแบบ <span class="text-strong">Real Time</span> ได้
					และไม่มีระบบแจ้งเตือนหากห้องเย็นมีปัญหา<br><br>
					
					ถ้ามีระบบ IoT ผู้บริหารหรือเจ้าของห้องเย็นจะดูอุณหภูมิได้ทันทีแบบ Real Time ในเวลานั้นๆ ตลอดเวลา 24 ชั่วโมง ผ่านมือถือสมาร์ทโฟน นอกจากนี้ยังดูอุณหภูมิย้อนหลังได้จะได้รู้พฤติกรรมการทำงานของ
					เครื่องห้องเย็น คอมเพรสเซอร์ ฮีตเตอร์ คอล์ยเย็น คอล์ยร้อนทำงานอย่าง ดูผ่านกราฟได้  ประหยัดค่าใช้จ่ายเรื่องการบริหาร และค่าใช้จ่ายค่าแรงที่ต้องจ้างคนมาดูแลได้เยอะเลยทีเดียว<br><br>
					
					
					<span class="topic-article"> 5. เจ้าของห้องเอง ไม่มีเวลาดูแลห้องเย็น</span><br>	
					เจ้าของห้องเย็นหลายๆ คน มีห้องเย็นแต่ให้ลูกน้องดูแลแทนในส่วนนี้เราต้องเชื่อมั่นให้ลูกน้องดูแล แต่จริงๆ แล้วเราสามารถติดตามผลการทำงานของลูกน้องได้ ห่างๆ อย่างห่วงๆ ได้ ด้วยระบบ IoT<br><br>
					
					<span class="topic-article">สรุป</span><br>	
					สมาร์ทห้องเย็นจะช่วย <span class="text-strong">อำนวยความสะดวก</span> ให้หลายๆ ฝ่าย ทั้งผู้บริหาร ผู้แล และผู้ปฏิบัติงานห้องเย็น เราสามารถทำนายการทำงานของห้องเย็นได้โดยการสังเกตุพฤติกรรมของห้องเย็นเก็บเป็นสถิติ
					ลดความเสียง ประหยัดเวลาและค่าใช้จ่ายในการดูแลห้องเย็น<br><br>	
