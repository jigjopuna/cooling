					<h2>สุดยอดเทคนิคการซ่อมห้องเย็น</h2>
						สัญลักษณ์ไฟแสดงเตือนหน้าห้องเย็น บ่งบอกอะไรเราได้บ้าง ปัญหาที่เกิดขึ้น สาเหตุของปัญหา และวิธีการแก้ไข บทความนี้เหมาะกับผู้ที่สนใจดูแลห้องเย็นเองเบื้องต้น และมีเนื้อหาเชิงลึกไปบ้าง ผมจะอธิบายให้เข้าใจง่ายๆ แล้วกันครับ<br><br>

ก่อนจะเข้าเนื้อหาผมขออธิบายภาพรวมคร่าวๆ การดูแลรักษา การซ่อม ก่อนนะครับ การดูแลห้องรักษาห้องเย็นมี 2 ระดับ คือ การดูแลรักษาประจำปี (ในกรณีที่ห้องเย็นยังไม่มีปัญหา)​ กับการซ่อมบำรุง (กรณีห้องเย็นเสียแบบกระทันหัน)​<br><br>

ซึ่ง 2 ระดับนี้ วิธีการดูแลห้องเย็นความรู้ทักษะการแก้ปัญหาจะไม่เท่ากัน ซึ่งในบทความนี้ผมจะอธิบายการซ่อมบำรุง แบบห้องเสียกระทันหันนะครับ <br><br>

ส่วนการบำรุงรักษาประจำปีนั้นเป็นการดูแลเบื้องต้น 5 ข้อ นี้ คลิกอ่านคลิปได้เลย<br><br>

ห้องเย็นที่เราใช้งานปกติ อยู่ๆ ห้องเย็นไม่เย็น หรือ เครื่องทำความเย็นหยุดทำงานไปเลยซะอย่างนั้น เมื่อเกิดปัญหาอย่างนี้ เจ้าของหรือผู้ดูแลห้องเย็น อาจจะตกใจทำอะไรไม่ถูก เพราะกลัวของในห้องเย็นจะเสียหาย 

มาวิเคราะห์ปัญหากันเลยนะครับ จริงๆ ปัญหาห้องเย็นมีได้หลากหลายปัญหา แต่ผมจะมีวิธีการบีบปัญหาให้แคบลงแล้วหาต้นตอของปัญหานั่นให้เจอ<br><br>

ขอเริ่มจากปัญหาหลักๆ ที่พบมากที่สุดก่อนเลย คือห้องเย็นไม่เย็น เช่น ปกติทำงานอยู่ที่ -​18 องศา แต่ตอนนี้อุณหภูมิไม่เย็นเลย (10 องศา เป็นต้น)​ เครื่องคอมเพรสเซอร์ทำงานปกติ และไม่มีไฟสัญญาณอะไรติดหรือแสดงที่ตู้คอนโทรลเลย<br><br>

อาการนี้สันนิฐานเบื้องต้นได้ 2 อย่าง คือน้ำยารั่ว หรือ น้ำแข็งตันที่คอล์ยเย็น คลิกอ่าน 2 อาการนี้เพิ่มเติมได้<br><br>+


กับอีกอาการหนึ่งคือ เครื่องหยุดทำงานและมีสัญญานไฟสีแดงขึ้นที่หน้าตู้คอนโทรล

ดูว่าไฟสัญญาณสีแดงดวงไหนติด ก็ให้แก้ปัญหากับอุปกรณ์เหล่านั้น สัญญาณแจ้งเตือนที่ทาง Topcooling ทำจะมีอยู่ 5 ดวง ตามรูปด้านล่างนี้เลยครับ
<br>
1. คอมเพรสเซอร์กระแสไฟเกิน
2. พัดลมคอล์ยร้อนกระแสไฟเกิน
3. พัดลมคอล์เย็นกระแสไฟเกิน
4. ระบบไฟฟ้าไม่เสถียร
5. แรงดันน้ำยาผิดปกติ<br><br>


