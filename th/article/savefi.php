					ห้องเย็นประหยัดไฟ

					ห้องเย็นเป็นส่วนสำคัญมากในการประกอบธุรกิจด้านอาหาร หรือสินค้าที่ต้องใช้ห้องเย็น ลูกค้าท่านใดที่มีห้องเย็นอยู่แล้วหรือกำลังตัดสินใจซื้อห้องเย็น ปัจจัยหนึ่งที่ลืมไม่ได้เลยนั่นก็คือ  <span class="text-strong">ค่าไฟฟ้า</span> ที่ใช้กับห้องเย็น <br><br>
					เดือนๆ หนึ่ง ห้องเย็นเล็กๆ ขนาดกลาง หรือขนาดใหญ่ ค่าเริ่มต้นที่ หลักพันบาทแน่นอน ถ้าขนาดใหญ่ก็หลายหมื่นจนถึงหลักแสนก็มี<br><br>

					ถ้าเราประหยัดค่าไฟได้นั่นหมายถึงต้นทุนลดลง <span class="text-strong">กำไรก็เพิ่มขึ้น</span> นั่นเอง มาดูกันว่าเราจะลดค่าไฟฟ้าได้ยังไงบ้าง <br><br>

					<span class="bolds">1. ตั้งค่าอุณหภูมิให้เหมาะสมกับสินค้า</span><br>
					<span class="bolds">2. ตั้งค่าช่วงเวลาการละลายน้ำแข็ง Defrost</span><br>
					<span class="bolds">3. ตั้งค่าช่วงเวลาในการตัดและต่อเครื่องคอมเพรสเซอร์</span><br>
					<span class="bolds">4. เปิดปิดประตูให้น้อยลงถ้าเป็นไปได้</span><br>
