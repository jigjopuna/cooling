					<br><br>
					
					<img src="../../content/images/art_iot/iot/iot_graph02.jpg" alt="กราฟห้องเย็น">
					<br><br>

					สังเกตุกราฟไหมครับว่ามีจุดที่สูงกว่าหมายเลขที่ 2 สูงไปถึงหมาย​ลขที่ 3 รู้ไหมครับว่าตรงนี้เกิดอะไรขึ้น... 
					ก็คือช่วงที่คอยล์เย็น defrost นั้นเอง

					<br><br>


					<h2>4. เปิดปิดประตูให้น้อยลงถ้าเป็นไปได้ </h2>
					ถ้าเปิดปิดประตูห้องเย็นน้อยความเย็นไม่ออกไปนอกห้องก็จะช่วยประหยัดไฟได้ครับ
					<br><br>

					<h2>5. ไม่กระแทกผนังห้องเย็นจนบุบหรือเป็นรอยรั่ว</h2>
					การเก็บสินค้าในห้องเย็นก็จะมีภาชนะใส่สินค้า เช่น ตะกร้า พาเลท  หรืออื่นๆ พนักงานไม่ระวัง โยนสินค้าไปกระแทกผนังห้องเย็นบุบ ยุบ หรือมีช่องให้ลมเย็น รั่วไหลออกไปได้ ฉะนั้นให้ระมัดระวังในการเคลื่อนย้ายสินค้าด้วย
					<br><br>


					<h2>6. ทำความสะอาดรังผึ้งแผงคอยร้อนสม่ำเสมอ</h2>
					เมื่อเครื่องทำเย็นใช้งานไปได้ สักระยะ 6 เดือนถึง 1 ปี <span class="text-strong">ฝุ่นจะเกาะ</span> ที่แผงระบายความร้อนบริเวณชุดคอนเด็นซิ่งหรือคอล์ยร้อน ฝุ่นจะหนาขึ้นเรื่อยๆ เราต้องการประหยัดค่าไฟฟ้า เราต้องทำความสะอาดแผงคอยล์ร้อน อาจจะเป่าลมก็ได้ เพียงเท่านี้ห้องเย็นก็จะระบายความร้อนได้ดีขึ้น ค่าไฟจะประหยัดไปได้แน่นอน เพราะเครื่องทำความเย็นทำงานน้อยลงนั้นเอง

