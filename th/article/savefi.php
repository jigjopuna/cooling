					<h2> 3. ตั้งค่าช่วงเวลาในการตัดและต่อเครื่องคอมเพรสเซอร์</h2>

					ควรตั้งค่าการหยุดทำงาน และกลับมาทำงานอีกครั้งของคอมเพรสเซอร์ให้ห่างกัน สัก 6-8 องศา  <span class="text-strong">(ขึ้นอยู่กับสินค้าด้วยว่า ยอมรับช่วงอุณหภูมิที่ห่างกันได้ไหม)</span>

					โดยปกติแล้วห้องเย็นแบบห้องชิลแช่ผักผลไม้ จะใช้อุณหภูมิประมาณ 5 องศา และเมื่ออุณหภูมิได้ 5 องศาแล้วเครื่องจะหยุดทำงาน จนกระทั่งอุณหภูมิร้อนขึ้นจนไปถึง 10 องศา (เป็นค่าสมมติ)​ เครื่องจะกลับมาทำงานอีกครั้ง แสดงว่าเหตุการณ์นี้ตั้งค่าให้ห่างกันแค่ 5 องศา
<br><br>
					ถ้าเราต้องการประหยัดไฟ หลักการก็คือให้คอมเพรสเซอร์หรือเครื่องทำความเย็นสตาร์ทให้ไม่บ่อยเกินความจำเป็น แบบว่า <span class="text-strong">เดี๋ยวตัด เดี๋ยวต่อ</span> เดี๋ยวตัด เดี๋ยวต่อ แบบนี้แสดงว่าเครื่องสตาร์ทบ่อย เราอาจตั้งค่า Diff ของอุณหภูมิน้อยเกินไป หรืออาจเกิดจากห้องเย็นรั่วก็เป็นได้ ที่ทำให้เครื่องตัดต่อบ่อย
<br><br>
					แต่ลูกค้าบางท่านต้องการให้อุณหภูมิคงที่ 5 องศา เป็นสินค้าที่ sensitive อุณหภูมิห้ามแกว่งไปจากนี้เกิน 1-2 องศา แบบนี้ถ้าต้องการประหยัดไฟจริงๆ ต้องใช้คอมเพรสเซอร์แบบ <span class="text-strong">Inverter</span> แต่คอมฯ พวกนี้ราคาจะสูงมาก ที่นี่ก็ต้องมาเทียบความคุ้มค่า ระหว่างค่าไฟฟ้ากับราคาคอมเพรสเซอร์แบบไหนจะคุ้มหรือเหมาะกับเรามากกว่ากัน 
<br><br>
					มาลองดูกราฟห้องเย็นของลูกค้า Topcooling <span class="text-strong">ที่เก็บแช่อาหารทะเล</span> ดูนะครับ ใช้อุณหภูมิ -​20 องศา จากรูป จุดที่ 1 คืออุณหภูมิที่ต้องการ เมื่อถึงแล้วเครื่องจะหยุดทำงาน อุณหภูมิจะสูงขึ้นจนไปถึงหมายเลขที่ 2 จากนั้นเครื่องจะกลับมาทำงานอีกครั้ง
<br><br>
					สังเกตุกราฟไหมครับว่ามีจุดที่สูงกว่าหมายเลขที่ 3 สูงไปถึงหมาย​ลขที่ 4 รู้ไหมครับว่าตรงนี้เกิดอะไรขึ้น... 
