					<span class="bolds">8. ระบบไฟฟ้าเสถียร</span><br><br>

										
					<h2> 1. ตั้งค่าอุณหภูมิให้เหมาะสมกับสินค้า</h2>
					สินค้ามีหลายประเภทแต่ละประเภทใช้อุณหภูมิการเก็บไม่เท่ากัน เช่น เก็บผักผลไม้ โดยทั่วไปจะใช้แค่ 3 ถึง 8 องศาช่วงนี้ แต่ถ้าเราตั้งไว้ที่ต่ำกว่า 0 องศา นอกจากจะทำให้เครื่องคอมเพรสเซอร์ทำงานนานและใช้ไฟฟ้ามากขึ้นแล้ว ยังทำให้สินค้าเสียหายอีกด้วย<br><br>
	
					<span class="text-strong">ฉะนั้นการตั้งค่า</span> อุณหภูมิห้องเย็นแนะนำให้ตั้งค่าตามอุณหภูมิที่สินค้าต้องการ จะช่วยประหยัดไฟได้ครับ<br><br>


					<h2>2. ตั้งค่าช่วงเวลาการละลายน้ำแข็ง Defrost</h2>
					ห้องเย็นที่ใช้อุณหภูมิต่ำกว่า 5 องศา ลงไป เช่น -​10 หรือ -​25 องศาเป็นต้น ทุกห้องจะต้องมีระบบ<span class="text-strong">ละลายน้ำแข็ง</span> หรือเรียกว่าดีฟรอสต์ ส่วนใหญ่จะใช้ฮีทเตอร์ทำความร้อนเพื่อละลายน้ำแข็งบริเวณคอยล์เย็นในห้องเย็น

					ที่ต้องละลายน้ำแข็งก็เพื่อไม่ให้น้ำแข็งไปเกาะที่คอยล์เย็น  หากเกิดน้ำแข็งเกาะหนาขึ้นจะทำให้ลมเย็นไม่ถูกส่งผ่านไปในห้องเพราะถูกน้ำแข็งกั้นบังเอาไว้
					<br><br>
					การจะประหยัดไฟได้นั้นต้องทำให้ฮีทเตอร์ทำงานให้ <span class="text-strong">น้อยที่สุด</span> เพราะฮีทเตอร์ใช้กำลังไฟฟ้าค่อนข้างสูง ถ้าเทียบง่ายๆ ก็จะคล้ายเตารีด หรือ เครื่องปิ้งย่างไฟฟ้า อุปกรณ์เหล่านี้มีความร้อนจะใช้กำลังไฟฟ้าสูง<br><br>

					<span class="text-strong">แล้วเราจะรู้ได้อย่างไง</span> ว่าฮีทเตอร์ทำงานตอนไหนบ้าง โดยปกติแล้วจะอยู่ที่การตั้งค่าตัวควบคุมห้องเย็นครับ ที่ใช้กันแพร่หลายก็จะมีอยู่ 3 ยี่ห้อ CAREL, DIXELL และ Elliwel 
					<br><br>
					ห้องเย็นที่ผมตั้งค่ามาตราฐานไว้เลยคือ Defrost ทุกๆ <span class="text-strong">3 ชั่วโมง</span> ครั้งละ 30 นาที คำถามถัดมาถามว่า จำเป็นต้องตั้งค่าเท่านี้ตลอดเลยไหม ตอบเลยว่าไม่จำเป็นต้องตั้งแบบนี้เป๊ะๆ ก็ได้ครับ อาจจะตั้งที่ 4 ชั่วโมง ครั้งละ 20 นาทีก็ได้ เพียงแต่สังเกตุให้น้ำแข็งไม่เกาะที่คอยล์เย็นก็เป็นอันใช้ได้
					<br><br>
					<span class="boldun">ข้อสังเกตุ</span> ถ้าห้องเย็นอุณหภูมิยิ่งต่ำมาก -​25 องศา น้ำแข็งก็จะเกาะเร็วกว่าอุณหภูมิสูง 
					<br><br>
					ขออนุญาตยกตัวอย่างห้องเย็น ที่ลูกค้าใช้แช่เก็บชิ้นส่วนไก่ เก็บในห้องเย็นแค่พักสินค้าระยะสั้นๆ 1 คืน ห้องเย็นใช้แค่ -​5 ก็พอ ไม่ให้ไก่แข็งมาก ทีนี้น้ำแข็งในคอยล์เย็นก็จะไม่ค่อยมีแล้ว เราอาจตั้งค่า defrost ไว้ที่ 5 ชั่วโมง ครั้งละ 15 นาทีก็ยังได้ 
					<br><br>
					กับอีก 1 ห้อง ลูกค้าใช้เก็บสมุนไพรจีน ที่ 5 องศา ห้องนี้ลูกค้าบอกว่าแทบไม่ต้อง Defrost เลย เพราะที่คอยล์เย็นไม่เกิดน้ำแข็ง
					<br>
					<span class="boldun">ฉะนั้น</span> อยากให้ลูกค้าสังเกตุห้องเย็นของตัวเองว่าการ defrost เป็นอย่างไรบ้าง อย่าลืมนะครับ ถ้าฮีทเตอร์ทำงานน้อยเท่าไรยิ่งประหยัดไฟมากขึ้นเท่านั้น
