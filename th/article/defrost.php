					<span class="bolds boldun"></span> 
					<h2></h2>
					
					

					ถ้า <span class="text-strong">น้ำแข็งตัน</span>  คอยล์เย็นจะเกิดปัญหาอะไรบ้าง
					แน่นอนครับคอยล์เย็นจะส่งลมเย็นไปในห้องไม่ได้เพราะติดน้ำแข็งที่ตันอยู่ ส่งผลให้ห้องเย็นไม่เย็น ไม่ได้อุณหภูมิที่ต้องการ สินค้าอาจเสียหายได้ มาดู <span class="text-strong">สาเหตุ</span> กันครับ<br><br>
					
					<strong>1. วางคอยล์เย็นผิดตำแหน่ง</strong><br>
					<strong>2. ปริมาณสินค้าในห้องเย็นมากเกินไป</strong><br>
					<strong>3. สเปคคอยล์เย็น ระยะฟินคอยล์ถี่เกินไป</strong><br>
					<strong>4. ตั้งค่า defrost ระยะเวลาละลายน้ำแข็งน้อยเกินไป</strong><br>
					<strong>5. ฮีทเตอร์น้อยเกินไป หรือทำงานไม่ปกติ</strong><br><br>



					<h2>1. ตำแหน่งวางคอยล์เย็น (Cooler unit) </h2>
						ตำแหน่งที่ถูกต้องคือตำแหน่งที่ไม่อยู่ <span class="text-strong">ฝั่งเดียวกับประตู</span> เพราะตอนที่เราเปิดประตูห้องเย็น อากาศความร้อนและความชื้นจะเข้าจับที่คอยล์เย็น ทำให้น้ำแข็งสะสมที่คอยล์เย็นเร็วกว่าปกติ<br><br>

					ให้วางคอยล์เย็นตรงข้ามประตูหรือด้านข้างก็ได้จะช่วยลดน้ำแข็ง ลดเวลาการทำงานของฮีทเตอร์ด้วยถ้ามีน้ำแข็งน้อย นั้นก็หมายถึงประหยัดค่าไฟไปด้วยนะครับ แถมห้องเย็นทำงานได้ประสิทธิภาพสูงสุดด้วย<br><br>

					<h2>2. จำนวนของในห้องเย็น</h2>
					สินค้าในห้องเย็นมีหลากหลายประเภท หากสินค้านั้นมี <span class="text-strong">ความชื้นสูง</span> ห้องเย็นจะดึงความชื้นนั้นออก จะมาสะสมความชื้นที่คอยล์เย็นเป็นน้ำแข็ง<br><br>

					ส่วนใหญ่จะเป็นของสดเช่น ปลา เนื้อ ไก่ หมู ที่เป็นอาหารสดๆ ยังไม่ได้ถูกแช่แข็งมา ของสดเหล่านี้จะมีน้ำหรือความชื้นสูงทำให้เกิดน้ำแข็งเร็วกว่าปกติ <br><br>

					<span class="text-strong">วิธีการป้องกันคือ</span> ถ้าเป็นสินค้าสดให้ทยอยนำเข้าห้องเย็นครั้งละ 500 ถึง 1,000 กิโลกรัม หรือปริมาณที่เหมาะสมกับขนาดคอมเพรสเซอร์และระยะห่างฟินคอยล์เย็น จะเกี่ยวข้องกับข้อที่ 3<br><br>

					<h2>3. สเปคคอยล์เย็น</h2>
					การเลือกคอยล์เย็นต้องเลือกให้ <span class="text-strong">เหมาะสม</span> กับสินค้าที่จะเก็บนะครับ ต้องรู้ว่าเราใช้อุณหภูมิเท่าไร กี่องศา เช่น เราแช่ผลไม้ใช้อุณหภูมิแค่ 5 องศา โอกาส <span class="text-strong">น้ำแข็งตัน</span> จะมีน้อยกว่าห้องเย็นประเภทฟรีส<br><br>

					ถ้าเราใช้อุณหภูมิ -18 หรือ -20 องศา คอยล์เย็นมีน้ำแข็งแน่นอน เราก็เลือกระยะห่างฟินคอยล์ให้กว้างหน่อยเช่น 6 หรือ 7 มิลลิเมตร เป็นต้น<br><br>
					
					<img src="../../content/images/article/defrost/ice2.jpg" style="" alt="ฮีทเตอร์ห้องเย็น"><br><br>

					<h2>4. การตั้งค่าเวลาละลายน้ำแข็ง</h2>
					การตั้งค่าเป็นส่วน <span class="text-strong">สำคัญมาก</span> อย่างหนึ่งไม่แพ้ข้ออื่นเลย เพราะหากเราวางตำแหน่งคอยล์เย็นดีแล้ว เลือกคอยล์เย็นให้เหมะกับสินค้าแล้ว แต่ตั้งค่าไม่ถูกต้องก็ทำให้น้ำแข็งตันคอยล์เย็นได้เหมือนกัน<br><br>

					<span class="text-strong">การตั้งค่า</span> ส่วนใหญ่ที่ผมตั้ง เอาเป็นส่วนใหญ่แล้วกันนะครับ เพราะแต่ละห้องก็จะตั้งค่าไม่เหมือนกัน อยู่ที่ความเหมาะสม ผมจะตั้งค่า <span class="text-strong">Defrost</span> ไว้ที่  ทุกๆ 3 ชั่วโมง ครั้งละ 20 ถึง 30 นาที <br><br>

					การตั้งค่านี้ <span class="text-strong">ขึ้นอยู่กับ</span> การใช้งานนะครับ ผมจะให้ลูกค้าสังเกตห้องเย็นนของตัวเอง บางทีตั้งเวลา 3 ชั่วโมงอาจจะน้อยไป เพราะ 3 ชั่วโมงน้ำแข็งนิดเดียวเอง ก็เลื่อนไปเป็น 4 หรือ 5 ชั่วโมง ละลายครั้ง
					จะช่วยประหยัดค่าไฟฟ้าได้ด้วย ลดการ Defrost โดยไม่จำเป็น<br><br>

