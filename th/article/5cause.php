					<h2>5 สาเหตุหลักที่ห้องเย็นไม่เย็น</h2>

						<span class="bolds">1. ใช้ห้องเย็นไม่สอดคล้องกับสินค้าเก็บ </span> <br />
						<span class="bolds">2. คอล์ยเย็นตัน</span>  <br />
						<span class="bolds">3. คอล์ยร้อนตัน </span> <br />
						<span class="bolds">4. น้ำยาทำความเย็นรั่วไหล </span> <br />
						<span class="bolds">5. มีสิ่งของขวางกั้นที่ระบายลมร้อน </span> <br /><br />

						<h2>1. ใช้ห้องเย็นไม่สอดคล้องกับสินค้าที่เก็บ หมายถึงอะไร</h2>

						เพื่อให้เข้าใจง่ายและเห็นภาพมากขึ้น
						ขอยกตัวอย่าง case study เรามีห้องเย็นหนึ่งห้อง ขนาด <span class="text-strong">2.4 x 4.0 x 2.4</span> เมตร กว้างยาวสูง สเปคที่ใช้งานปกติทั่วไปจะมีดังนี้ <br /> <br />

						- คอมเพรสเซอร์ขนาด 2.5HP ถึง 3HP (แรงม้า)  <br />
						- พัดลมคอล์ยร้อน 1 ใบพัด <br />
						- คอล์ยเย็น 1 ตัว 2 พัดลม <br />
						- ฉนวนโฟม PU หนา 4 นิ้ว รองรับ -25 องศา <br />
						- น้ำยา R404a หรือ R22 <br /> <br />
						
						<img src="../../content/images/speedlock/install-466x272pxyen.jpg" alt="คอยล์เย็น ห้องเย็น"><br><br>

						เรามีไก่สด <span class="text-strong">หมูสด</span> 1,200 กิโลกรัม หรือ 1.2 ตัน
						เปิดเครื่องห้องเย็นไว้ <span class="text-strong">-18 องศา</span> จากนั้นใส่ไก่สด หมูสด เข้าไปแช่แข็งไว้ในห้องเย็น แช่ทิ้งไว้ 12 ชม.
						ปรากฏว่าไก่กับ <span class="text-strong">หมูเขียว</span> ทั้งหมดเลย สาเหตุเพราะอะไรรู้ไหมครับ <br /> <br />

						เพราะว่าขนาดเครื่องทำความเย็นออกแบบมาสเปคตามด้านบนนั้น จะแช่ของสดได้ไม่เกิน <span class="text-strong">400 ถึง 500 กิโลกรัมต่อวัน</span> กับขนาดเครื่องคอมเพรสเซอร์ 3 แรง ถึงจะเก็บสินค้าได้เย็นและแข็งอย่างมีคุณภาพ <br /> <br />

						หรือถ้าหมูหรือไก่ที่ <span class="text-strong">ฟรีสแข็ง</span> มาแล้ว ก็จะเก็บแช่แข็งได้ประมาณ 800 ถึง 900 กิโลกรัมต่อวัน เพราะสินค้าที่แข็งมาแล้วความร้อนในตัวสินค้าจะน้อยกว่าของสดๆ  <br /> <br />
						
						<img src="../../content/images/article/5cause/033.jpg" alt="ห้องเย็นแช่หมู"><br><br>

						จากโจทย์ที่ต้องการแช่เป็นของสดจำนวน 1.2 ตันจะต้องใช้เครื่องทำความเย็นเท่าไร กับห้อง 4 เมตร <br /> <br />

						ถ้าเก็บ 1.2 ตัน ต่อวัน จะต้องใช้คอมเพรสเซอร์อย่างน้อย 5HP และพัดลมระบายความร้อนที่คอยล์ร้อน 2 พัดลม <br /> <br />

						จริงๆ เรามีวิธีคิด <span class="text-strong">คำนวณ</span> หาขนาดของเครื่องทำความเย็นนะครับ ตามด้านล่างนี้เลย <br /><br />

						ข้อมูลที่ควรทราบก่อนทำห้องเย็น <br />
						<span class="bolds">- อุณหภูมิที่ต้องการ </span> <br />
						<span class="bolds">- ปริมาณสินค้าต่อวัน/กิโลกรัม </span> <br />
						<span class="bolds">- ขนาดของห้องเย็น กว้าง ยาว สูง </span> <br />
						<span class="bolds">- อุณหภูมิสินค้าก่อนเข้าห้องเย็น </span> <br />
						<span class="bolds">- ระยะเวลาลดลดอุณหภูมิ </span> <br /><br />

						คลิกอ่านเพิ่มเติมเงื่อนไขการทำห้องเย็น ผมเขียนไว้แยกอีกบทความหนึ่ง <br /> <br />

						<span class="boldun">สรุป</span> ข้อ 1 นี้ สาเหตุที่ห้องไม่เย็นไม่เย็นเพราะเก็บหมูและไก่สด เกินปริมาณที่เครื่องทำความเย็นจะทำได้ เหมือนตัวอย่างในรูปด้านบนที่ลูกค้าใส่ของสดจนแน่นห้องเลย<br /> <br />

						 <h2>2. คอล์ยเย็นตัน  </h2>
						คอล์ยเย็นตันอาการเป็นยังไง
						<span class="text-strong">อาการคือลมเป่าไม่ออก</span> เพราะน้ำแข็งเกาะที่ครีบ (FIN) บริเวณตัวคอยล์เย็นจนลมไม่สามารถผ่านได้ อาการนี้จะเกิดกับห้องเย็นที่ใช้อุณหภูมิต่ำๆ -18 หรือ -25 องศา เป็นต้น <br /><br />

						 <span class="text-strong">สาเหตุ</span>  ที่น้ำแข็งเกาะ เนื่องจากว่าในห้องเย็นและในตัวสินค้ามีความชื้น คอยล์เย็นจะเอาความชื้นและความร้อนออกข้างนอก และความชื้นนี้เองจะเปลี่ยนเป็นน้ำแข็งมาเกาะที่คอยล์เย็น <br /><br />

						<span class="boldun">วิธีแก้ไข </span><br />
						ให้กดปุ่ม manual ละลายน้ำน้ำแข็ง ที่ตู้คอนโทรล ฮีทเตอร์จะละลายน้ำแข็งให้หมดไป จะเวลาประมาณ 15 ถีง 25 นาที ในการละลาย 

						จริงๆ ฮีทเตอร์จะทำงานโดยอัติโนมัติอยู่แล้ว ขึ้นอยู่กับการตั้งค่าเช่นให้ทำการละลายทุกๆ 3 หรือ 4 ชั่วโมงเป็นต้น <br /> <br />
						
					

						<span class="boldun">หมายเหตุ  </span><br />
						ถ้าฮีทเตอร์กำลังทำงานอยู่เครื่องทำความเย็น คอมเพรสเซอร์ Compressor และคอยล์เย็น Cooler Unit จะหยุดทำงาน ระหว่างนี้ <span class="text-strong">ไม่ให้ไปปิดสวิตซ์</span> ห้องเย็นนะครับ เพราะฮีทเตอร์จะหยุดทำงาน และถ้าเปิดสวิตซ์ให้เครื่องทำงานใหม่อีกครั้ง น้ำแข็งจะค้างอยู่ ยังไม่ได้ละลาย ทำให้คอยล์เย็นตันสะสม <br /> <br />

						<h2>3. คอล์ยร้อนตัน</h2>
						คอล์ยร้อนเป็นอุปกรณ์ที่อยู่ภายนอกห้องเย็น นึกภาพง่ายๆ ก็เหมือนแอร์ตามบ้านที่ต้องติดคอยล์ร้อนอยู่ข้างนอก ห้องเย็นก็เหมือนกัน อาการถ้าคอล์ยร้อนตัน ก็จะไม่สามารถระบายความร้อนออกจากระบบทำความเย็นได้ <br /> <br />

						<span class="boldun">วิธีแก้ไข </span> <br />
						ให้ <span class="text-strong">เป่าลม</span> หรือฉีดน้ำความความสะอาดที่แผงรังผึ้ง ถ้าเป็นน้ำไม่ใช่ฉีดที่ตัวเครื่องหรือพัดลมนะครับ ให้ฉีดที่เป็นอลูมิเนียม ซี่ๆ อ่ะครับ ที่มีฝุ่นเกาะอยู่ เท่านี้ห้องเย็นก็เย็นเหมือนเดิมแล้วครับ <br /> <br />


						<h2>4. น้ำยาทำความเย็นรั่วไหล</h2>
						ห้องเย็นเมื่อเราใช้งานไปสักระยะหนึ่งท่อน้ำยาที่เป็นท่อทองแดงอาจมีรูรั่วได้ เช่นยกของไปโดนท่อน้ำยา หรือมีอะไรไปกระแทก อาจจะเป็นรูเล็กๆ <span class="text-strong">น้ำยาจะคอยๆ ซึมออกเรื่อยๆ</span> จนหมด อาจใช้เวลาเป็นเดือน สองเดือน หรือ รูรั่วใหญ่น้ำยาก็จะไหลออกหมดเร็วขึ้น  <br /> <br />

						หากน้ำยารั่วหมด ก็จะไม่มีน้ำยาที่ทำความเย็นไปฉีดในห้องเย็น ทำให้ห้องเย็นไม่ได้เย็นได้ <br /> <br />
						<span class="boldun">วิธีแก้ไข </span><br />
						ให้  <span class="text-strong">หารูรั่ว </span> ให้เจอแล้วเชื่อมรั่วได้เลย จากนั้นเต็มน้ำยาเข้าระบบ แค่นี้ห้องเย็นก็เย็นเหมือนเดิมแล้วครับ
						วิธีหารูรั่วคลิกอ่านบทความนี้ต่อได้ครับ <br /> <br />

						<h2>5. มีสิ่งของขวางกั้นที่ระบายลมร้อน</h2>
						เป็นอีกสาเหตุที่ผมพบบ่อยๆ เนื่องจากว่าห้องเย็นบางห้อง ลูกค้าไว้ใน <span class="text-strong">อาคาร</span>  ที่มีสิ่งกีดขวางที่ระบายความร้อนเช่น <span class="text-strong">กำแพง</span>  ทำให้ลมร้อนไม่มีที่ระบาย ส่งผลให้น้ำยาที่เข้าไปฉีดในห้องเย็นไม่เย็นเพียงพอ ทำให้ไม่ได้อุณหภูมิที่ต้องการครับ<br /><br />
						
						แนะนำว่าควรจะวางห้องเย็นไว้ในที่ที่สามารถ <span class="text-strong"> อาคารระบายลมร้อน </span>  ได้ครับ จะช่วยให้เครื่องทำความเย็นไม่ต้องทำงานตลอดเวลา จะประหยัดค่าไฟฟ้าได้ และได้อุณหภูมิที่เย็นตามที่ต้องการ<br /><br />
						

						<span class="boldun">สรุป </span> <br />
						ทั้ง 5 หัวข้อที่ได้พูดถึง เป็นสาเหตุหลักและพบได้บ่อยๆ กับการใช้ห้องเย็น หากห้องเย็นมีปัญหา แนะนำให้ลองเช็คห้องเย็นตามหัวข้อนี้ดูได้ครับ <br />
