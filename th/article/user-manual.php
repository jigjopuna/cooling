					 <h2> หมายเหตุ </h2>
					  1. ห้ามทำการปรับแต่งเทอร์โมสตัท เทอร์โมเอ็กซ์แพนชั่นวาล์ว ดีฟรอสไทเมอร์	<br>
						  โอเอร์โหลด ไฮ-โลเพรสเชอร์คอนโทรล โยพลการเด็ดขาดเพราะระบบได้ SET ไว้เรียบร้อยแล้ว<br><br>	
					  2.  ขอให้ทำการดับเครื่องทันที หากมีสิ่งใดผิดปรกติดังต่อไปนี้	<br>
					ก) พัดลมคอล์ยเย็นหยุดหมุน โดยที่เครื่องทำความเย็นยังทำงานอยู่	<br>&nbsp;&nbsp;&nbsp;
					ข) พัดลมคอนเดนเซอร์หยุดหมุน โดยที่เครื่องทำความเย็นยังทำงานอยู่<br>		&nbsp;&nbsp;&nbsp;
					ค) มีเสียงดังผิดปรกติที่คอมเพรสเซอร์	<br>&nbsp;&nbsp;&nbsp;
					ง) มีน้ำแข็งจับขาวที่คอมเพรสเซอร์ทั้งลูก<br>&nbsp;&nbsp;&nbsp;
					<br><br>

					<strong>(3) การหยุดเดินเครื่องทำความเย็น</strong><br>&nbsp; &nbsp;&nbsp;
					<strong>3.1 ปิดสวิทซ์ควบคุม </strong>ไปทาง "OFF" ไฟสีเขียวจะดับ และเครื่องทำความเย็นทุกส่วนจะหยุดการทำงานทันที	<br>&nbsp; &nbsp;&nbsp;
					<strong>3.2 เปิดฝาตู้ </strong>และยกเบรคเกอร์ลงมาตำแหน่ง "OFF"<br><br>

					<h2>ใช้ห้องเย็นให้มีประสิทธิภาพ</h2>
					<strong>ข้อปฎิบัติในการใช้ห้องเย็นอย่างมีประสิทธิภาพ</strong><br>&nbsp; &nbsp;&nbsp;
					<strong>1)	ทำการปิดประตูห้องเย็น</strong> ทุกครั้งเมื่อเข้าหรือออกจากห้องเย็น เพื่อป้องกันไม่ให้ <span class="text-strong">ความเย็นรั่วไหล</span>  ทำให้เครื่องทำความเย็นต้องทำงานหนักเกินไป	<br>
					&nbsp; &nbsp;&nbsp;
					<strong>2)	เวลาเปิดประตูห้องเย็น</strong> ไม่ควร <span class="text-strong">รวบม่านพลาสติก</span> เพราะจะทำให้ความเย็นรั่วออกมาจากในห้อง<br>
					&nbsp; &nbsp;&nbsp;
					<strong>3)	ตรวจเช็คห้องเย็น </strong>ทุกครั้งก่อนทำการล็อคกุญแจด้านนอกเพื่อป้องกันคน <span class="text-strong">ติดค้างด้านใน</span> <br>&nbsp; &nbsp;&nbsp;
					<strong>4)	 กรณีที่ท่านติดค้าง</strong> อยู่ภายในห้องเย็นเปิดไม่ออก ท่านสามารถผลักลูก <span class="text-strong">กระทุ้งที่บานประตู</span> เพื่อให้ประตูเปิดออกได้	<br>&nbsp; &nbsp;&nbsp;
					<strong>5)	ห้ามใช้สิ่งของ</strong> กระแทกหรือชนประตูห้องเย็น เพราะจะทำให้บานประตูเสียหายได้<br>&nbsp; &nbsp;&nbsp;
					<strong>6)	ห้ามหมุนหรือปรับอุปกรณ์ </strong> ควบคุมโดยพลการ เพราะจะทำให้ระบบอัตโนมัติ และการทำงานของเครื่องทำความเย็นผิดปรกติ และเกิดความเสียหายแก่เครื่องทำความเย็น และสินค้าที่เก็บไว้ภายใน	<br>&nbsp; &nbsp;&nbsp;
					<strong>7)	ห้ามนำสิ่งของต่าง ๆ </strong>ไปขวางทางระบายอากาศของชุดคอนเดนเซอร์<br>&nbsp; &nbsp;&nbsp;
					<strong>8)	ควรแจ้งช่าง </strong> ผู้ที่มีความชำนาญให้ทำการแก้ไขทันทีที่เกิดความผิดปรกติ<br>&nbsp; &nbsp;&nbsp;
