					<h2>การต่อสายไฟ</h2>	
					การต่อสายไฟ (ELECTRICLE WIRING)<br>
					&nbsp; &nbsp;&nbsp;
					<strong>1. ใช้ไฟฟ้า</strong> กระแสตรงหรือกระแสสลับขนาด 12 โวลท์ (12 vac/dc) โดยผ่านหม้อแปลงไฟฟ้า (Tranformer)   เข้า 220 โวล์ ออก 12 โวล์ (220V/12V)<br><br>
					&nbsp; &nbsp;&nbsp;
					<strong>2. สายสัญญาณ</strong>  (ฆenser) เป็น NTC(C) 2-wire <span class="text-strong">จำนวน 2 เส้น</span>  สำหรับวัดอุณหภูมิห้อง 1 เส้น และวัดอุณหภูมิที่คอล์ยเย็น 1 เส้น ไม่ควรเดินสายสัญญาณร่มกับสายไฟ เนื่องจากคลื่นไฟฟ้าไปรบกวนค่าความ ต้านทานของสายสัญญาณ ทำให้การแสดงค่าอุณหภูมิคลาดเลื่อนไม่คงที่ตลอดเวลา หากมีความจำเป็น	ต้องต่อสายไฟเนื่องจากความยาวของสายสัญญาณไม่เพียงพอ ควรใช้สายไฟที่มีฉนวนหนาหุ้ม
					<br><br>
					&nbsp; &nbsp;&nbsp;
					<strong>3. ไม่ควรให้น้ำ</strong> หรือของเหลวซึมผ่านเข้าไปภายในส่วนที่เป็นโลหะ เนื่องจากจะทำให้ <span class="text-strong">สายสัญญาณชื้น</span>  และเสียหายทำให้การแสดงอุณหภูมิไม่ตรงกับความเป็นจริง หากมีความจำเป็นที่ต้องติดตั้งในพื้นที่  ที่มีความชื้นสูงมาก ควรใช้กาวซิลิโคนยอดตรงรอยต่อระหว่างส่วนที่เป็นยางซิลิโคนกับส่วนที่เป็นโลหะ
