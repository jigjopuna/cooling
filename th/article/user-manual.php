					<h2>ตรวจสภาพก่อนเดินเครื่อง</h2>
					<strong>(1) การตรวจสภาพก่อนเดินเครื่องทำความเย็น มีข้อปฎิบัติดังนี้</strong><br>
					&nbsp; &nbsp;&nbsp;<strong>1.1 เข้าไปในห้องเย็น</strong> ตรวจดูสภาพที่ <span class="text-strong">คอล์ยเย็น</span> โดยเฉพาะพัดลมคอล์ยเย็น ไม่ให้มีสิ่งกีดขว้างการหมุนของพัดลม<br>
					&nbsp; &nbsp;&nbsp;<strong>1.2 ออกจากห้องเย็น</strong> และปิดประตูห้องเย็นให้สนิท<br>
					&nbsp; &nbsp;&nbsp;<strong>1.3 ตรวจดูชุดคอนเดนซิ่งยูนิต</strong> ว่ามีสิ่งใดกีดขว้างการหมุนของมอเตอร์คอมเพรชเชอร์  และพัดลมคอนเดนเซอร์หรือไม่ หากมีต้องกำจัดออก<br>
					&nbsp; &nbsp;&nbsp;<strong>1.4 ลองผลักสวิตซ์</strong> "RESET" ของชุด HI-LO PRESSURE CONTROL ดู<br>
					&nbsp; &nbsp;&nbsp;<strong>1.5 ตรวจดูฟิวส์</strong> ของระบบไฟคอนโทรล และของระบบไฟเมนว่าอยู่ในสภาพปกติหรือไม่ หากผิดปกติต้องเปลี่ยนใหม่ให้เรียบร้อย<br>
					&nbsp; &nbsp;&nbsp;<strong>1.6 กดสวิตซ์ละลายน้ำแข็ง</strong> ด้วยวิธี <span class="text-strong">MANUAL</span> ที่ตัว ELIWELL เครื่องจะละลายน้ำแข็งเอง<br><br>

					<strong>(2) การเปิดเครื่องทำความเย็น มีขั้นตอนการปฎิบัติดังนี้</strong>	<br>
					&nbsp; &nbsp;&nbsp;<strong>2.1 หลังจากตรวจสภาพ</strong> ก่อนเดินเครื่องเรียบร้อยแล้ว <span class="text-strong">ให้ยกเบรคเกอร์</span> (BREAKER) ของ MAIN POWER ที่จ่ายเข้าสวิตซ์บอร์ดไปที่ตำแหน่ง "ON" และยกเบรกเกอร์ ในตู้สวิตซ์ควบคุมการทำงานไปที่ตำแหน่ง "ON" เช่นกัน	<br><br>
					&nbsp; &nbsp;&nbsp;<strong>2.2 จากนั้นปิดฝาตู้</strong> ให้เรียบร้อย แล้วปิดสวิตซ์สีเขียวที่หน้าตู้ไปที่ตำแหน่ง "ON" ไฟเขียว หน้าตู้จะสว่าง รอประมาณ 4 นาที พัดลมคอล์ยเย็นในห้องจะทำงานพร้อมกับ ชุดคอนเดนซิ่งยูนิต โดยอัตโนมัติ	<br><br>
					&nbsp; &nbsp;&nbsp;<strong>2.3 ตรวจระดับน้ำมัน</strong> ในคอมเพรสเชอร์ที่วาล์วดูน้ำมัน ต้องมีระดับน้ำมันที่น้อยกว่า 1  ใน 2 <span class="text-strong"> ของดาวัว</span>  หากระดับต่ำกว่า 1 ใน 4 ต้องรีบหยุดเครื่อง และทำการเติมน้ำมัน  จนได้ระดับ จึงเริ่มทำการเดินเครื่องได้อีกครั้ง	<br><br>

					&nbsp; &nbsp;&nbsp;<strong>2.4 เฝ้าดูการทำงานสักครู่</strong> ดูว่าอุณหภูมิห้องเย็นลดลงมาเรื่อย ๆ จนถึงอุณหภูมิห้องตามที่ต้องการหรือไม่ หากไม่ลดลงมาเรื่อย ๆ แสดว่าต้องมีอะไรผิดปรกติ ให้เปิด ประตูห้องดูพัดลมคอล์ยเย็นหมุนตามปกติหรือไม่ และดูว่าเครื่องทำความเย็นมีเสียง ดังผิดปรกติหรือไม่ แต่ถ้าอุณหภูมิลดลงเรื่อย ๆ ตามปรกติ และไม่มีเสียงดังผิดปรกติแล้ว  ก็เป็นอันเสร็จเรียบร้อยให้เครื่องทำงานต่อไป
					<br><br>
					&nbsp; &nbsp;&nbsp; <strong>2.5 การทำงาน</strong> ของเครื่องทำความเย็นจะเป็นไปโดยอัตโนมัติดังนี้ <br>							
					เมื่อเครื่องทำความเย็นให้กับห้องเย็น จน <span class="text-strong">อุณหภูมิลดลง</span> ตามที่ต้องการแล้วเทอร์มสตัทจะสั่งให้ชุดคอนเดนซิ่งยูนิตหยุดทำงาน ส่วนพัดลมคอล์อยเย็นในห้องเย็นยังคงทำงานอยู่ จนเมื่ออุณหภูมิในห้องเย็นเพิ่มขึ้นจากเดิมประมาณ 3 ถึง 5 องศาเซลเซียส เทอร์โมสตัทจะสั่งต่อ ให้ชุดคอนเดนซิ่งยูนิตทำงานอีกครั้ง 
					<br><br>
					เมื่ออุณหภูมิลดลงจนถึงอุณหภูมิที่ต้องการ <span class="text-strong">เทอร์มสตัท</span> ก็จะสั่งให้ชุดคอนเดนซิ่งหยุดทำงานอีกครั้ง และจะวนวียนอยู่เช่นนี้ไปเรื่อย ๆ ตราบใดที่เราเปิดเครื่องทำความเย็นอยู่ 								
					แต่เมื่อเครื่องทำความเย็นไปในระยะเวลาหนึ่งประมาณ <span class="text-strong"> 4 ถึง 6 ชั่วโมง</span> จะเกิดน้ำแข็งเกาะ ที่คอล์อยเย็น <br>	<br>	
					เนื่องจากไอน้ำห้องเย็น <span class="text-strong">กลั่นตัวเป็นหยดน้ำ</span> และถูกทำความเย็นจนน้ำแข็งเกาะที่คอล์ยเย็นดังนั้นในระบบจึงมี "DEFROST TIMER" ไว้สำหรับสั่งฮิตเตอร์ให้หยุดการทำงานทุกๆ 4 ถึ 6 ชั่วโมงตามที่เราตั้งไว้ เพื่อขจัดน้ำแข็งที่เกาะคอล์ยเย็นออกไป โดยจะสั่งให้ชุดเครื่องทำความเย็นหยุดการทำงานประมาณ 20 ถึง 30 นาที								
																
					และเมื่อครบเวลาก็จะสั่งหยุดการทำงานของเครื่อง <span class="text-strong">ฮีตเตอร์ </span>และสั่งให้ชุดเครื่องทำความเย็น ทำงานอีกครั้ง ทั้งนี้เพื่อให้พัดลมที่หมุนเวียนผ่านคอล์ยเย็นสามารถถ่ายเทความเย็นได้สะดวก 	ขณะที่การละลายน้ำแข็งนี้ไฟโชว์สีแดงหน้าตู้จะสว่างไฟเขียวจะดับ	<br>	<br>								
					&nbsp; &nbsp;&nbsp;<strong>2.6 ในกรณีที่เกิด</strong> <span class="text-strong">น้ำแข็งเกาะ</span> ที่คอล์ยเย็นมาก ๆ และเมื่อใช้การละลายน้ำแข็งระบบอัตโนมัติแล้วยังไม่พอ ให้ช่วยลายน้ำแข็งด้วยวิธี MANUAL โดยกดสวิตซ์ละลายน้ำแข็งที่ตัว Temp Control ค้างไว้ 5 วินาที ไฟสีแดงจะสว่าง ไฟสีเขียวจะดับ เครื่องทำความเย็นจะหยุดการทำงานและฮิตเตอร์จะทำงานต่อไปเมื่อน้ำแข็งละลายหมด เครื่องทำความเย็นจะกลับมาทำงานเองตามปรกติ								
