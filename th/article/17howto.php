				6. ใช้ไฟเท่าไหร่ต้องขอไฟฟ้าไหม<br>
				7. ห้องเย็นใช้ค่าไฟประมาณเท่าไหร่<br>
				8. อยากเห็นภาพส่งรูปให้ดูหน่อย<br>
