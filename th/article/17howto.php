				<span class="topic-article"> 9. เข้าไปดูของห้องจริงได้ไหม</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				ลูกค้าสามารถเข้าชมและลองใช้งานห้องเย็นก่อนได้ครับ โดยเดินทางที่ Topcooling ที่จังหวัดนครปฐม <a href="" target="_blank">ตามแผนที่นี้ได้เลย</a>
				
				<br><br>
				
				<span class="topic-article"> 11. สินค้าจะแข็งไหม (ห้องติดลบ)</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				สินค้าแต่ละประเภทจะแข็งในช่วงอุณหภูมิที่ <span class="text-strong">ไม่เท่ากัน</span> ยกตัวอย่างเช่น ไก่ แช่แข็งไก่สดทั้งตัว ซึ่งตัวไก่จะมีความหนา กว่าความเย็นจะเข้าไปข้างใน
				ต้องใช้อุณหภูมิ ติดลบถึง -15 ถึง -18 องศาเซลเซียส หรือจะให้แข็งมากกว่านี้ที่อุณหภูมิติดลบเยอะๆ ก็ต้องเป็นห้องบาสฟรีสครับ<br><br>
				
				หากเก็บสินค้าเป็นผักผลไม้ ก็ไม่จำเป็นต้องแข็ง แค่ -5 ถึง 0 องศา ก็เพียงพอ
				 
				<br><br>
				
				<span class="topic-article"> 12. ต้องเรียงของยังไงหรือมีชั้นไหม</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				ชั้นวางเป็นสิ่งจำเป็นที่ห้องเย็นควรมี เพราะเราจะได้จัดวางสินค้าได้เป็นระเบียบ ขนเข้าขนออกได้สะดวก 
				<br><br>
				
				<span class="topic-article"> 13. ถ้าห้องเย็นเสียจะรู้ได้ยังไง</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				โดยทั่วๆ ไปแล้ว เราจะรู้ว่าห้องเย็นจะเสียหรือมีปัญหาก็ต่อเมื่อเราเห็นสิ่งผิดปกติของเครื่องคอมเพรสเซอร์ หรือปัญหาจากระบบ เราต้องเดินไปดูหน้าห้องเย็น
				แต่เรากำลัง <span class="text-strong">พัฒนาระบบติดตามสถานะห้องเย็น</span> ได้ตลอดเวลาผ่านโทรศัพท์มือถือ และรู้สถานะการทำงานแต่ละอุปกรณ์ที่สำคัญของห้องเย็น <br><br>
				
				
				<img src="../../content/images/article/17howto/iot.jpg" alt="ห้องเย็นไฮเทค"><br><br>
				
				ระบบจะแจ้งเตือนเราไปยังแอปไลน์ หรือ เฟสบุค ที่ทุกคนใช้งานกันอยู่ทุกวัน จะบอกเราล่วงหน้าว่าจะเกิดอะไรขึ้นบ้าง โดยเราจะวัดกระแสและแรงดันการทำงานของ
				ระบบห้องเย็น หากมีแนวโน้มที่จะเกิดปัญหาระบบจะแจ้งเตือน เราจะได้แก้ไขปัญหาได้ทันท่วงที ก่อนที่จะ <span class="text-strong">เกิดปัญหา</span>  และทำความเสียหายสินค้าในห้องเย็น
				
				<br><br>
				
				<span class="topic-article"> 14. มีบริการหลังการขายไหม</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				ห้องเย็นมือสองเรารับประกันให้ 6 เดือน ห้องมือหนึ่งรับประกัน 1 ปีครับ 
				<br><br>
				
				<span class="topic-article"> 15.  ต้องจ่ายเงินยังไง</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				หากยืนยันสั่งซื้อห้องเย็น <span class="text-strong">มัดจำ 50%</span> ส่วนที่เหลือจ่ายที่หน้างานเมื่อติดตั้งและเดินเครื่องได้เรียบร้อย โดยสามารถโอนเข้าบัญชีธนาคารได้ หรือมามัดจำที่ 
				Topcooling ได้ อยู่นครปฐม ครับ
