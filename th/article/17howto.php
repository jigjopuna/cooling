				<span class="topic-article">1 . อยากได้ห้องเย็นมีขนาดและราคาเท่าไหร่บ้าง</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-strong">ห้องเย็น</span> ขนาดกว้าง 3 เมตร ยาว 3 เมตร สูง 2.4 เมตร จะเก็บสินค้าได้ประมาณ 2-3 ตัน ขึ้นอยู่กับเป็น <span class="text-strong">สินค้า</span> อะไร เนื้อสัตว์ หรือ ผักผลไม้ ถ้าเป็นประเภทผักผลไม้ น้ำหนักก็น้อยลงไป ส่วนนี้เป็นห้องเย็นสำเร็จรูปนะครับ
				จริงๆ แล้ว ก่อนเลือกห้องเย็นว่าจะเอาขนาดเท่าไร จะมี <span class="text-strong">ปัจจัย</span> ในการเลือกครับ เช่น ประเภทสินค้า, ปริมาณสินค้า(กิโลกรัม/ตัน) <br><br>
				
				อ่านต่อหัวข้อที่ 2 ได้เลย เนื้อหาจะต่อเนื่องกันในการเลือกขนาดห้อง ส่วนเรื่องราคาห้องเย็นสำเร็จรูปมือสองถูกๆ จะเริ่มที่ 130,000 บาท ครับ หรือดูราคาเพิ่มเติมได้ในข้อ 17<br><br>
				
				<span class="topic-article">2 . เก็บของ.....เท่านี้ใช้ห้องเย็นขนาดไหนดี</span><br>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
				ในข้อที่ 1 ได้พูดถึง <span class="text-strong">ปัจจัย</span> ในการเลือกขนาดห้องเย็น คือ ประเภทสินค้า และปริมาณที่จะเก็บ <span class="text-strong">ยกตัวอย่าง</span> ห้องเย็นสำเร็จรูปขนาดเล็กที่สุด 3 x 3 x 2.4 เมตร จะเก็บเนื้อสัตว์ได้ 2-3 ต้น ผักผลไม้ น้ำแข้ง ไอศรีม ได้จะ 1-2 ตัน
				หากเก็บหรือแช่แข็ง <span class="text-strong">สินค้าที่มากๆ</span>  หลายสิบตัน จำเป็นต้องขยายห้องเย็น และจะแนะนำเป็น <span class="text-strong">ห้องเย็นแบบฝัง</span> ถ้าเก็บสินค้าเยอะๆ  แล้วห้องเย็นแบบฝังกับห้องเย็นสำเร็จรูปต่างยังไงให้อ่านข้อ 3 ต่อครับ
