					<h2>ห้องเย็นไฟฟ้า 3 เฟส</h2>
					<span class="bolds">ไฟฟ้า 3 เฟส</span>  กับห้องเย็นเป็นของคู่กัน <span class="text-strong">ทำไม</span> ต้องเป็น 3 เฟส ด้วยล่ะ 1 เฟส (single phase) ใช้ไม่ได้หรอ และปัญหาที่ลูกค้าพบบ่อยๆ ติดตามได้ในบทความนี้ครับ<br><br>

					
					<span class="boldun">การขอไฟฟ้า 3 phase</span> ต้องรีบขอกับทางการไฟฟ้าใกล้ๆ บ้าน เพราะต้อง <span class="text-strong">ใช้เวลาในการขอ</span>  และติดตั้งมิเตอร์ เวลาที่สร้างห้องเย็นเสร็จจะได้ใช้ห้องเย็นได้เลย ข้อมูลการเตรียมเอกสารขอไฟฟ้า <a href="https://www.pea.co.th/c1/%E0%B9%80%E0%B8%81%E0%B8%B5%E0%B9%88%E0%B8%A2%E0%B8%A7%E0%B8%81%E0%B8%B1%E0%B8%9A%E0%B9%80%E0%B8%A3%E0%B8%B2/%E0%B9%81%E0%B8%9C%E0%B8%99%E0%B8%87%E0%B8%B2%E0%B8%99%E0%B9%81%E0%B8%A5%E0%B8%B0%E0%B9%82%E0%B8%84%E0%B8%A3%E0%B8%87%E0%B8%81%E0%B8%B2%E0%B8%A3/ArtMID/3809/ArticleID/16212/%E0%B8%AB%E0%B8%A5%E0%B8%B1%E0%B8%81%E0%B9%80%E0%B8%81%E0%B8%93%E0%B8%91%E0%B9%8C%E0%B8%81%E0%B8%B2%E0%B8%A3%E0%B8%82%E0%B8%AD%E0%B9%83%E0%B8%8A%E0%B9%89%E0%B9%84%E0%B8%9F%E0%B9%83%E0%B8%AB%E0%B8%A1%E0%B9%88-%E0%B8%A1%E0%B8%B4%E0%B9%80%E0%B8%95%E0%B8%AD%E0%B8%A3%E0%B9%8C%E0%B8%8A%E0%B8%B1%E0%B9%88%E0%B8%A7%E0%B8%84%E0%B8%A3%E0%B8%B2%E0%B8%A7-%E0%B9%80%E0%B8%9E%E0%B8%B4%E0%B9%88%E0%B8%A1%E0%B8%82%E0%B8%99%E0%B8%B2%E0%B8%94-%E0%B9%82%E0%B8%AD%E0%B8%99%E0%B9%80%E0%B8%9B%E0%B8%A5%E0%B8%B5%E0%B9%88%E0%B8%A2%E0%B8%99%E0%B8%8A%E0%B8%B7%E0%B9%88%E0%B8%AD" target="_blank">คลิกดูได้ครับ</a> <br><br>
					
					วิดีโอเรื่องไฟฟ้า 3 เฟส ดูคลิปด้านล่างนี้ได้เลยครับ<br><br>
					
					<iframe width="100%" height="400" src="https://www.youtube.com/embed/P2scN3vSw70" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe><br><br>
					
					<h2>ทำไมต้องใช้ไฟ 3 phase (เฟส)</h2>
					ไฟ 3 phase จะดีกว่า single phase (1 เฟส) ในหลายๆ ข้อ<br>
					<span class="boldun">1. ไฟตก ไฟกระชาก</span><br> 
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
					โอกาสเกิด ไฟตก ไฟกระชาก กับไฟ 3 เฟสจะน้อยกว่า single phase เนื่องจากว่า single phase เป็นไฟฟ้า 220 v ที่ใช้ร่วมกับเครื่องใช้ไฟฟ้าภายในบ้าน บ้านส่วนใหญ่จะ <span class="text-strong">แชร์ไฟฟ้า</span> ที่อยู่ในละแวกเดียวกัน เช่นหากแถวนั้นมีงานวัดที่ใช้ไฟฟ้ามากๆ สังเกตว่าจะมี <span class="text-strong">ไฟตกเป็นช่วงๆ</span> หากเกิดเหตุการณ์แบบนี้บ่อยครั้งก็จะไม่ค่อยดีกับเครื่องทำความเย็น ถึงแม้เราจะมีระบบป้องกัน (Phase Protect) อยู่แล้วแต่ก็ไม่แนะนำที่เป็น single phase ถ้าเป็นไปได้ หรือหากจำเป็นต้องใช้ single phase จริงๆ ก็ใช้ได้แต่ต้องมั่นใจว่าระบบไฟเสถียร
					<br><br>
					<span class="boldun">2. ภาระโหลดกระแสไฟฟ้า</span><br>
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
					ไฟฟ้า 3 phase มี 4 เส้น ไฟ 3 เส้น และนิวตรอนอีก 1 เส้น สมมุติเราใช้มิเตอร์ 15/45 Amp ยกตัวอย่าง ห้องเย็นเราใช้ 12 Amp จะ <span class="text-strong">แบ่งภาระกระแสไฟฟ้า</span> เส้นละ 4 แอมป์ เมื่อเทียบกับ single phase ที่ใช้กระแสไฟ 12 แอมป์ในเส้นเดียว ก็จะเกิด <span class="text-strong">ความร้อน</span> ของขดลวดที่สายไฟทำให้เกิดพลังงานความร้อนสูญเสีย ส่งผลให้ค่าไฟอาจสูงขึ้นได้ครับ <br><br>
					
					<span class="boldun">3. ช่วยลดอุปกรณ์ไฟฟ้าที่ไม่จำเป็นออก</span><br>
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
					ไฟฟ้า 3 เฟส <span class="text-strong">ไม่ต้องใช้ cab</span>  ช่วยสตาร์ตคอมเพรสเซอร์ขณะเริ่มทำงาน (Capacitor Start) ส่วน single phase จะต้องใช้ แคปสตาร์ทช่วย <span class="text-strong">คอมเพรสเซอร์</span>  ขณะเริ่มทำงาน และหากห้องเย็นใช้ไปนานๆ 3-4 ปี single phase หากแคปเสียขึ้นมาจะทำให้เครื่องทำความไม่ทำงาน ก็ต้องหมั่นดูแลเครื่องทำความเย็นบ่อยขึ้น<br><br>

					<span class="bolds">ความแตกต่างระหว่าง 1 เพส กับ 3 เฟส มีประมาณนี้ครับ</span> <br><br>
					<span class="boldun">สรุป</span>
					ไฟฟ้า 3 เฟส จะดีกว่า 1 เฟสในหลายๆ ด้าน แต่ก็ต้องแลกกับค่าติดตั้งมิเตอร์ 3 เฟส ในครั้งแรกประมาณเกือบ 2 หมื่นบาท

					<br><br>
					<h2>ราคา</h2>
					หากเป็นห้องเย็นไม่เกิน 6 เมตรแนะนำเป็นขนาด ไฟ 3 เฟส 4 เส้น 15/45 แอมป์<br><br>
					<img src="../../content/images/article/3phase.jpg" alt="ห้องเย็น ไฟฟ้า"><br><br>

					<h2>ขยายเขต</h2>
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
					หากสถานที่ที่จะใช้งานห้องเย็นไม่ได้อยู่ <span class="text-strong">แนวเสาไฟฟ้า 3 เฟสผ่าน</span> ให้สังเกตุดูสายไฟ จะมีอยู่ 4 เส้น นี่แหละคือไฟฟ้า 3 phase หากไม่มีจะต้องขอขยายเขตกับทางการไฟฟ้า ในส่วนนี้จะมี <span class="text-strong">ค่าใช้จ่าย</span> ในการขยายนะครับ ค่าใช้จ่ายก็จะขึ้นอยู่กับระยะทางหรือระยะห่างจากสถานที่ที่จะใช้ห้องเย็นกับเสาไฟหลัก 3 phase<br><br>

					<span class="text-strong">คำว่าขยายเขต</span> นั้นหมายถึงหน้าบ้านหรือหน้าโรงงาน ฝั่งเรามีเสาไฟฟ้า 3 เฟสอยู่พร้อมลากสายไฟเข้ามิเตอร์หน้าโรงงานเราได้เลย ไม่ต้องขยายเขต

					แต่ถ้าเสาไฟ 3 เฟสอยู่คนละฝั่งหรืออยู่ฝั่งตรงข้าม อันนี้ต้องขยายเขตครับ


					<br><br>
					<h2>เวลา</h2>
					&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
					ระยะเวลาตั้งแต่วันที่ขอไฟฟ้า 3 phase แล้ว กี่วันการไฟฟ้าถึงจะมาติดตั้งมิเตอร์ให้

					อันนี้ก็จะขึ้นอยู่การไฟฟ้าแต่ละท้องถิ่นใช้เวลาประมาณ 30 วัน ทั้งขยายเขตและติดตั้งมิเตอร์ 15/45 แอมป์
					
