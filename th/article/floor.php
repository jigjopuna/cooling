					<h2>พื้นห้องเย็นควรเป็นแบบไหนดี</h2>
					
					พื้นเป็นส่วนสำคัญมากของห้องเย็น เราต้องวางสินค้าบนพื้นในห้องเย็นตลอดเวลา เราต้องเลือกพื้นให้เหมาะสมกับการใช้งานของเรา เพื่อประสิทธิภาพสูงสุดในการใช้ห้องเย็น มีวิธีเลือกยังไงบ้าง พื้นมีแบบไหนบ้าง แต่ละแบบต่างกันแค่ไหนมาดูกัน<br><br>

					พื้นห้องเย็นมีกี่แบบ พื้นห้องเย็นมี 2 แบบ<br>
					1.พื้นสำเร็จรูป <br>
					พื้นสำเร็จรูปจะใช้วัสดุเป็นแผ่นอลูมิเนียมลายกันลื่นปูบนฉนวนโฟมห้องเย็น <br><br>

					ข้อดีคือ <br>
					-ติดตั้งง่าย สะดวกรวดเร็ว เหมาะกับห้องเย็นขนาดเล็ก ถึงกลาง <br>
					- ราคาถูกกว่าพื้นปูน<br><br>

					ข้อด้อยของพื้นสำเร็จรูป<br>
					-ทนแรงกระแทกไม่ได้มาก<br>
					-กันน้ำซึมลงพื้นได้ไม่ดีเท่าพื้นปูน<br><br>



					2.พื้นปูน จะใช้ปูนคอนกรีตเทลงบนฉนวนโฟม<br>

					การออกแบบพื้นปูนนั้นต้องออกแบบให้ดีตั้งแต่ก่อนทำห้องเย็น เพราะรายละเอียดจะมีมากกว่าพื้นแบบสำเร็จรูป<br>

					ข้อดีของพื้นปูนคือ<br>
					-แข็งแรง  ทนแรงกระแทกของสินค้าในห้องเย็นได้เช่นการวางตะกร้าสินค้าหรือเป็นพาเลทได้<br>
					-กันนน้ำซึมลงใต้ห้องเย็นได้ดีมาก<br>
					-รองรับน้ำหนักต่อตารางเมตรได้มากกว่าพื้นสำเร็จรูป<br><br>

					จุดด้อยของพื้นปูน<br>
					-ใช้เวลาติดตั้งนานกว่าพื้นสำเร็จรูป<br><br>

					ที่นี่เงื่อนไขต่อมาก็คือระดับพื้นปูน ถ้าเราวางโฟมแล้วเทปูนเลย ความสูงต่างจากพื้นข้างนอกค่อนข้างมาก 
					โฟมหนา 10 ซม. ปูนหนาอีก 10 ซม. รวมกันก็ 20 ซม.<br><br>

					ถ้าอยากให้พื้นห้องเย็นมีระดับเท่ากับพื้นนอกห้องเย็นเราจะต้องขุดหลุมเพื่อวางโฟมกับเทปูน จะขุดหลุมลึกกี่เซ็นติเมตรเพื่อวางโฟมและเทปูนหนากี่เซ็น เพื่อให้จบออกมาแล้วเสมอกับพื้นด้านนอกก็จะขึ้นอยู่ที่เราออกแบบเช่น
					อุณหภูมิที่ใช้ในห้องเย็นจะตัวกำหนดความกนาของฉนวนโฟม<br><br>

					ถ้าพื้นระดับเสมอกันจะสะดวกมากตอนนำของเข้าเดินเข้า เข็นรถเข็น หรือรถโฟคลิฟท์เข้าก็สะดวกจะดีกว่าพื้นสำเร็จรูป
					แต่ถ้าเอาง่ายๆ ไม่ซีเรียสเรื่องระดับ ก็วางโฟมแล้วเทปูนเลยก็ได้<br><br>

					ห้องเย็นที่ทำพื้นปูนได้ต้องห้องเย็นแบบฝังคือห้องเย็นที่ต้องไปติดตั้งหน้างานเท่านั้นหากเป็นห้องเย็นสำเร็จรูปแบบเคลื่อนย้ายได้จะทำเป็นพื้นปูนไม่ได้<br><br>

					สเปคปูนที่ต้องการสำหรับห้องเย็นต้อง strength  ขั้นต่ำ 380 ksc ซึ่งจะสูงกว่าพื้นปูนทั่วไป ทำไมถึงต้อง 380 เนื่องจากว่าห้องเย็นเป็นอุณหภูมิติดลบ แบบแช่แข็งมากๆ อุณหภูมิมีผลกับพื้นปูน ถ้าไม่ถึง 380 พื้นปูนอาจร่อนหรือลอกได้ <br><br>

					อีกอย่างหนึ่งถ้าทำปูนแล้วอาจจะมีอีพร็อกซีด้วยก็ได้อันนี้เป็นออฟชั่นเสริมจะไม่ทำก็ได้ 
					ข้อดีของอีข้อแม้
					พื้น eproxy จะไม่มีเชื้อจุลินทรีวิวห้องเย็นไลน์ผลิตจึงนิยมใช้ ทำความสะอาดง่าย น้ำไม่ซึมผ่านอีพร็อกซีลงปูนด้านล่างแต่พื้นปูนที่เคลือบด้วยอีพร็อกซีก็มาพร้อมกับต้นทุนที่สูงขึ้นเหมือนกัน<br><br>

					แต่ส่วนใหญ่ที่ผมทำห้องเย็นมากหลายๆ ห้อง ลูกค้าจะชอบพื้นสำเร็จรูป เพราะห้องที่ผมทำจะไม่ค่อยเกิน 10 เมตร <br><br>

					ก็หวังว่าบทความนี้จะช่วยตัดสินใจในการเลือกพื้นห้องเย็นได้บ้าง ^^
