Patkol พร้อมระบบทำความเย็นครบชุด + รถเข็น/ชั้นแสตนเลสและอื่นๆพร้อมใช้งานสภาพดีมาก
- ระบบน้ายาฟรีออน
- ซื้อมาเมื่อปี 2555 ราคา 3 ล้านบาท/ตู้ ไม่รวมงานติดตั้ง
- กำลังการผลิต 120-300 กก./ช.ม (ฟรีสกุ้งชุบเกล็ดขนมปัง)
- อุณหภูมิห้อง -40 ถึง -65 องศา C
ขายถูกมาก 1.3-1.4 ล้านบาท/ตู้ มีของทั้งหมด 2 ตู้ / ไม่รวมงานรื้อถอนและติดตั้ง สาเหตุ โรงงานเลิกกิจการ (อยู่ที่สมุทรสาคร)