						<span class="bolds">2. ตั้งค่า Defrost ละลายน้ำน้ำแข็งไม่ถูกต้อง</span> <br />
						<span class="bolds">3. ฮีทเตอร์ละลายน้ำแข็งไม่ทำงาน</span> <br />
						<span class="bolds">4. ปิดเครื่องทำความเย็นก่อนจะถึงรอบ ละลายน้ำแข็ง </span> <br />
						<span class="bolds">5. แช่เก็บสินค้าที่มีความชื้นมากๆ </span> <br /><br />

						<h2>1. ความชื้นเข้าไปในคอยล์เย็นมากเกินไป</h2>
						ความชื้นเป็นสาเหตุหลักๆ เลยที่ทำให้น้ำแข็งเกิดขึ้น โดยเฉพาะที่คอยล์เย็นที่ทำหน้าที่ <span class="text-strong">ฉีดน้ำยา</span> ทำความเย็น ถ้าเป็นห้องเย็นที่อุณหภูมิติดลบด้วยแล้ว
						น้ำแข็งจะก่อตัวแถวๆ คอยเย็นอันนี้เป็นเรื่องปกติครับผม <br /><br />
						
						นอกจากนี้ปัญหาที่ผมเจอบ่อยๆ เลย คือตอนเปิดประตูห้องเย็น อากาศและความชื้นจากภายนอกจะเข้าไป ยิ่งถ้าคอยล์เย็นอยู่ใกล้ๆ กับประตูด้วยแล้ว ความชื้นจะเข้าคอยล์เย็นโดยตรง ทำให้เกิดน้ำแข็งได้เร็วกว่าเดิมอีก
						<br/> <br/>
						

						 <h2>2. ตั้งค่า Defrost ละลายน้ำน้ำแข็งไม่ถูกต้อง </h2>
						ข้อนี้ก็เป็นปัญหาที่ <span class="text-strong">พบได้บ่อย</span> เหมือนกันครับ เพราะการละลายน้ำแข็งที่ฮีทเตอร์ทำงานน้อยเกินไปละลายน้ำแข็งไม่หมด ถ้าฮีทเตอร์ทำงานนานเกินไป ก็เกินความจำเป็นทำให้สิ้นเปลืองค่าไฟ
						<br /><br />
						<h3 style="font-size:36px;">การตั้งค่าที่แนะนำ</h3>
						<span class="boldun">- รอบการละลายน้ำแข็งที่ชั่วโมง</span><br />
						   ส่วนใหญ่ที่ผมตั้งก็จะ <span class="text-strong">3-4 ชั่วโมง</span> Defrost ทีหนึ่ง ไม่จำเป็นต้องตั้งค่านี้เสมอไป จริงๆ ขึ้นอยู่กับว่าน้ำแข็งก่อตัวเร็วแค่ไหน ถ้าใช้อุณหภูมิ +5 องศา น้ำแข็งเกิดช้า ก็ตั้งสัก 5-6 ชม. ก็ได้ <br /><br />
						<span class="boldun">- เวลาการ Defrost ต่อรอบ กี่นาที</span><br />
						  ผมจะตั้งไว้ประมาณ <span class="text-strong">20-30 นาที</span> แล้วแต่อุณหภูมิห้องที่ใช้ ถ้าติดลบมากๆ น้ำแข็งมากก็ตั้งไว้ 30 นาที ถ้าเป็นห้องเย็นชิว ก็ตั้ง 20 นาทีก็ได้ ค่านี้ปรับได้นะ ขึ้นอยู่กับหน้างานจริง <br /><br />
						<span class="boldun">- อุณหภูมิสูงสุดที่หยุดการ Defrost </span><br />
						 อันนี้ตั้งไว้สัก 30 องศา ถ้า Defrost แล้วเซ็นเซอร์ที่อยู่หลังคอยล์เย็นจับได้ว่าอุณหภูมิห้องเย็นบริเวณที่มีฮีทเตอร์ ตอนนี้สูงเกิน 30 องศาแล้ว ให้หยุดการ Defrost จริงๆ เงื่อนไขนี้จะใช้คู่กับเวลา Defrost ถ้าเข้าเงื่อนไขไหนก่อนก็จะหยุดการ Defrost 
						<br /><br />
						
						<h2>3. ฮีทเตอร์ละลายน้ำแข็งไม่ทำงาน</h2>
						ถ้าเกิดเหตุการณ์นี้ต้องเรียกช่างเข้าไปดูครับ<br /><br />
						
						<h2>4. ปิดเครื่องทำความเย็นก่อนจะถึงรอบ ละลายน้ำแข็ง</h2>
						เป็นอีกสาเหตุที่ทำให้น้ำแข็งไม่ละลาย เพราะว่าเราตั้งค่าเวลารอบการ Defrost ไว้ ถ้าเราปิดสวิตซ์ที่ตู้คอนโทรลก่อนถึงรอบ Defrost ระบบละลายน้ำแข็งจะไม่ทำงาน และถ้าเราเปิดสวิตซ์อีกครั้งเวลาจะเริ่มนับใหม่ เช่นตั้งไว้ทุกๆ 3 ชั่วโมง ก็รออีก 3 ชม. ถึงจะ Defrost เมื่อถึงจุดนี้ทำให้น้ำแข็งก่อตัวสะสมจากรอบ Defrost ที่แล้วไม่ได้ถูกละลาย
						<br /><br />
						<span class="boldun">วิธีแก้ไข </span><br />
						ถ้าเราปิดเครื่อง ก่อนที่เราจะเปิดเครื่องอีกครั้งให้ไปสังเกตุที่คอยล์เย็นว่ามีน้ำแข็งอยู่ไหม ถ้าอยู่ให้เราไปกดปุ่ม Manual Defrost ก็คือบังคับให้ละลายน้ำแข็งก่อนเลย แล้วจึงให้เครื่องทำงาน
						<br /><br />
						<h2>5. แช่เก็บสินค้าที่มีความชื้นมากๆ</h2>
						สินค้าที่มีความชื้นมากๆ ส่วนใหญ่จะเป็นสินค้าสด เช่น หมูสด ไก่สด อาหารทะเลสดๆ อาหารลักษณะนี้จะมีน้ำหรือความชื้นในตัวสินค้าสูง เมื่อนำมาเข้าห้องเย็นเลย ความชื้นจะมาเกาะที่คอยล์เย็นได้เร็วมาก เร็วกว่าอาหารที่แช่แข็งมาแล้ว
						
						<br /><br />
						<span class="boldun">วิธีแก้ไข </span><br />
						ให้ตั้งค่ารอบการละลายน้ำแข็งให้ถี่ขึ้นจะช่วยแก้ไขปัญหานี้ได้<br /><br />

						<span class="boldun">สรุป </span> <br />
						ทั้ง 5 หัวข้อที่ได้พูดถึง เป็นสาเหตุหลักและพบได้บ่อยๆ กับการใช้ห้องเย็น หากห้องเย็นมีปัญหา แนะนำให้ลองเช็คห้องเย็นตามหัวข้อนี้ดูได้ครับ <br />
