					<h2>5 สาเหตุหลักที่ให้ทำให้น้ำแข็งตันคอยล์เย็น</h2>
						ห้องเย็นที่ใช้ <span class="text-strong">อุณหภูมิต่ำกว่า 0 องศา</span> ในคอยล์เย็นจะเกิดน้ำแข็งเกาะ เนื่องจากอุณหภูมิในคอยล์เย็นจะต่ำอุณหภูมิห้องอยู่ 5 ถึง 7 องศาเซลเซียส ด้งนั้นปัญหาน้ำแข็งตันเกิดขึ้นได้ตามหัวข้อหลักต่อไปนี้ครับ <br /><br />
